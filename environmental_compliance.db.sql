CREATE VIEW air_quality_compliance AS
SELECT
    a.timestamp,
    a.location,
    a.pollutant,
    a.concentration,
    r.standard AS regulatory_standard,
    CASE
        WHEN a.concentration <= r.standard THEN 'Compliant'
        ELSE 'Non-Compliant'
    END AS compliance_status
FROM
    air_quality a
JOIN
    regulatory_standards r
ON
    a.pollutant = r.pollutant;
CREATE VIEW emission_compliance AS
SELECT
    e.timestamp,
    e.source_id,
    e.pollutant,
    e.emissions,
    r.standard AS regulatory_limit,
    CASE
        WHEN e.emissions <= r.standard THEN 'Compliant'
        ELSE 'Non-Compliant'
    END AS compliance_status
FROM
    emission_records e
JOIN
    regulatory_standards r
ON
    e.pollutant = r.pollutant;
CREATE VIEW air_quality_compliance_summary AS
SELECT
    location,
    pollutant,
    AVG(CASE WHEN compliance_status = 'Compliant' THEN 1 ELSE 0 END) * 100 AS compliance_percentage
FROM
    air_quality_compliance
GROUP BY
    location,
    pollutant;
CREATE VIEW emission_compliance_summary AS
SELECT
    location,
    pollutant,
    AVG(CASE WHEN compliance_status = 'Compliant' THEN 1 ELSE 0 END) * 100 AS compliance_percentage
FROM
    emission_compliance
GROUP BY
    location,
    pollutant;
